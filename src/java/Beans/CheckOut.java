package Beans;

/**
 *
 * @author Arthur
 */
public class CheckOut {

    private int id;
    private String date;
    private int status;

    public CheckOut() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
        
}
