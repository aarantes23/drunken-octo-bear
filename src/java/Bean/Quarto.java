public class Quarto {
    private int codigo;
    private String andar;
    private String tipo;
    private String quantAcom;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getAndar() {
        return andar;
    }

    public void setAndar(String andar) {
        this.andar = andar;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getQuantAcom() {
        return quantAcom;
    }

    public void setQuantAcom(String quantAcom) {
        this.quantAcom = quantAcom;
    }
    
    
}
